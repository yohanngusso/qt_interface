def soma(x: int, y: int) -> int:
    """Soma dois numeros.

    ARG:
        x: valor a ser somado
        y: alor a ser somado

    VARS:
        r: resultado da soma de x, y

    Função que soma ds números foi criada para suprir a
    necessidade de que existam outras funções.

    """
    r = x + y
    return r
